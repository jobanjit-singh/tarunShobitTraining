// Arithematice Operator

console.log(4 + 5);
console.log(4 - 5);
console.log(4 * 5);
console.log(4 / 5);
console.log(4 % 5);
console.log(4 ** 2);

console.log("--------InCREMENT OPERATOR---------");

// increament operators

let a = 5;
let b = 10;
console.log(a++);
console.log(a);

console.log(++a);
console.log(a);

console.log(++a * b++);
console.log(b);

console.log("----------CONDITIONAL OPERATOR----------");

console.log(4 > 3);
console.log(4 >= 3);
console.log(4 < 3);
console.log(4 <= 3);
console.log("------->>>-------");
console.log(4 == "4");
console.log(4 === 3);
// console.log(4 != 3);
// console.log(4 !== 3);

console.log("----------LOGICAL OPERATOR----------");
// && || !

let n = 4;
console.log(n > 2 && n < 10);
//          4 > 2 && 4 < 10
console.log(n > 2 || n < 10);
console.log(!(n > 2));

console.log("----------BITWISE OPERATOR----------");
// & | ^ ~ << >>
console.log(4 & 2); // 0
console.log(5 & 2); // 0

console.log(4 | 2);
console.log(5 | 2);

console.log(4 ^ 2);
console.log(5 ^ 2);

console.log(16 >> 3);
console.log(4 << 1);
console.log(16 << 2);
console.log(4 << 2);

console.log(~4);
console.log(~5);
console.log(~10);
console.log(~-10);
